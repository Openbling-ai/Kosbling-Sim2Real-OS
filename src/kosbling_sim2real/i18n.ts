export type UiLocale = "zh-CN" | "en-US";
export type DirectionLabel = "up" | "flat" | "down" | null;

export function resolveUiLocale(locale: string | undefined): UiLocale {
  return locale?.toLowerCase().startsWith("zh") ? "zh-CN" : "en-US";
}

export function getI18n(locale: string | undefined) {
  const uiLocale = resolveUiLocale(locale);
  return uiLocale === "zh-CN" ? zhCN : enUS;
}

